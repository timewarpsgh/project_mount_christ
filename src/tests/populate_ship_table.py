def add_ships_to_table_ship_template():
    for name, dict in hash_ship_name_to_attributes.items():
        new_obj = ShipTemplate(
            name=name,
            img_id=name,
            durability=dict['durability'],
            tacking=dict['tacking'],
            power=dict['power'],
            capacity=dict['capacity'],
            max_guns=dict['max_guns'],
            min_crew=dict['min_crew'],
            max_crew=dict['max_crew'],
            buy_price=dict['price'],
            required_industry_value=100,
        )


        SESSION.add(new_obj)

    SESSION.commit()


